    private void approveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveActionPerformed

        int selectedRow = returnJTable1.getSelectedRow();
        if (selectedRow < 0) {
            return;
        }
        ReturnRequest request = (ReturnRequest) returnJTable1.getValueAt(selectedRow, 0);
        OrderItem item = request.getOrderItem();
        Order order = request.getOrder();
        item.setMoney(item.getMoney() - request.getMoney() / item.getNumber());
        order.setTotalPrice(order.totalPrice());
        System.out.println(order.getTotalPrice());
        Payment payment = order.getPayment();
        payment.setTotal(order.getTotalPrice());
        request.setStatus("agreed");
        request.setReceiver(account);
        request.setResolveDate(new Date());
        populateTable();
    }//GEN-LAST:event_approveActionPerformed

