    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) returnJTable1.getModel();
        model.setRowCount(0);
        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()) {
            if (request instanceof ReturnRequest) {
                Object[] row = new Object[7];
                row[0] = (ReturnRequest) request;
                row[1] = ((ReturnRequest) request).getReason();
                row[2] = ((ReturnRequest) request).getMoney();
                row[3] = ((ReturnRequest) request).getDescribe();
                row[4] = ((ReturnRequest) request).getSender();
                row[5] = ((ReturnRequest) request).getRequestDate();
                row[6] = ((ReturnRequest) request).getStatus();
                model.addRow(row);
            }

        }
    }

