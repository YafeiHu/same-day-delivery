    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) returnJTable1.getModel();
        model.setRowCount(0);
        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()) {
            if (request instanceof RepairRequest) {
                Object[] row = new Object[6];
                row[0] = (RepairRequest) request;
                row[1] = ((RepairRequest) request).getReason();
                row[2] = ((RepairRequest) request).getDescribe();
                row[3] = ((RepairRequest) request).getSender();
                row[4] = ((RepairRequest) request).getRequestDate();
                row[5] = ((RepairRequest) request).getStatus();
                model.addRow(row);
            }
        }
    }

