    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) returnJTable1.getModel();
        model.setRowCount(0);
        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()) {
            if (request instanceof ReturnProductRequest) {
                Object[] row = new Object[7];
                row[0] = (ReturnProductRequest) request;
                row[1] = ((ReturnProductRequest) request).getReason();
                row[2] = ((ReturnProductRequest) request).getMoney();
                row[3] = ((ReturnProductRequest) request).getDescribe();
                row[4] = ((ReturnProductRequest) request).getSender();
                row[5] = ((ReturnProductRequest) request).getRequestDate();
                row[6] = ((ReturnProductRequest) request).getStatus();
                model.addRow(row);
            }
        }
    }

